// switch case


void main(){
int opt, num1, num2;
printf("1- multi, 2- add, 3- sub, 4- div\n");
scanf("%d", &opt);


printf("num1 and num2\n");
scanf("%d%d", &num1, &num2);



switch(opt){

{

case 1:
printf("multi-%d", num1*num2);
break;

case 2:
printf("add-%d", num1+num2);
break;

case 3:
printf("sub-%d", num1-num2);
break;

case 4:
printf("div-%d", num1/num2);
break;



}
    }
        }
